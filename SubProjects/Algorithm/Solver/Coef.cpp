#include "Coef.h"
using namespace std;
///////////////////////////////////////////////////////////////////////////////////////////////////
/// Coef
///////////////////////////////////////////////////////////////////////////////////////////////////
Coef::Coef() {
	r = 0;
	c = 0;
	val = 0;
}
Coef::Coef(int r, int c, real val) : r(r), c(c), val(val) {}
bool Coef::operator< (const Coef& b) {
	return this->r < b.r ||
		(this->r == b.r && this->c < b.c);
}
